void findShortestPath(int matrix[ROWS][COLS], Point source, Point dest) {
    int dr[] = {-1, 0, 1, 0};
    int dc[] = {0, 1, 0, -1};
    bool visited[ROWS][COLS];
    int distances[ROWS][COLS];
    for (int i = 0; i < ROWS; i++) {
        for (int j = 0; j < COLS; j++) {
            visited[i][j] = false;
            distances[i][j] = -1;
        }
    }
    visited[source.row][source.col] = true;
    distances[source.row][source.col] = 0;
    // Create a queue for BFS
    Point* queue = (Point*)malloc(sizeof(Point) * ROWS * COLS);
    int front = 0, rear = 0;
    queue[rear++] = source;
    while (front < rear) {
        Point current = queue[front++];
        if (current.row == dest.row && current.col == dest.col)
            break;
        // Explore all possible movements
        for (int i = 0; i < 4; i++) {
            int row = current.row + dr[i];
            int col = current.col + dc[i];
            if (isValid(row, col) && matrix[row][col] && !visited[row][col]) {// Check if the next movement is within the matrix boundaries and not visited
                visited[row][col] = true;
                distances[row][col] = distances[current.row][current.col] + 1;
                queue[rear++] = (Point){row, col};
            }
        }
    }
    Point current = dest;
    while (current.row != source.row || current.col != source.col) {
        sol[current.row][current.col] = 2;
        sol[dest.row][dest.col] = 9;
    display();
    delay(0.1);
    system("cls");
        for (int i = 0; i < 4; i++) {
            int row = current.row + dr[i];
            int col = current.col + dc[i];
            if (isValid(row, col) && distances[row][col] == distances[current.row][current.col] - 1) {
                current.row = row;
                current.col = col;
                break;
            }
        }
    }
}
